md"""The `layout` argument automatically associated each series with is own subplot.  However, we should balance these out, making the bottom wider.

Using the `grid` constructor, we can specify not only the `layout` of the subplots, but also each one's width and height.  

	heights = [0.2,0.8]

	widght = [0.8, 0.2]

... correspond to the _first_ row having 20% of the parent plot height, with the second row having 80% of parent plot height.  Likewise, the width will be 80% of the parent plot width for the first _column_ of the grid, with 20% for the second column of the grid.
"""

# ╔═╡ ca376d52-ae12-4e75-b09f-5cd920a695c3
md"""

	layout = grid(2,2, heights=[0.2, 0.8],widths=[0.8,0.2])

"""

